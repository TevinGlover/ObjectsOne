package object1;

public class Person {
    private String name;
    private int age;
    private int height;


        // Constructor
    public Person(String name, int age, int height) {
        this.name = name;
        this.height = height;
        this.age = age;
        }
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public int getHeight(){
        return height;
    }
    }




